    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        try {

            int codigo = Integer.parseInt(JOptionPane.showInputDialog(null, "Ingrese el código"));

            List<String> deuda = controlador.buscar(codigo);

            gestion = new VentanaGestionDeuda(deuda);
            this.setVisible(false);
            gestion.setVisible(true);

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Formato de código incorrecto, asegurate de ingresar datos numéricos");
        } catch (IndexOutOfBoundsException ex) {
            JOptionPane.showMessageDialog(null, "No se encontró ninguna deuda con el código ingresado");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    public float sumCantidad() {

        float suma = 0;
        int contador = tblDeuda.getRowCount();
        for (int i = 0; i < contador; i++) {
            if (tblDeuda.getValueAt(i, 3) != null) {
                suma = suma + Float.parseFloat(tblDeuda.getValueAt(i, 3).toString());
            }
        }

        return suma;
    }


