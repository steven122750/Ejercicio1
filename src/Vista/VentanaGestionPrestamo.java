    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        try {

            if (txtcodigo.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Digita el código para continuar");
                return;
            }

            int codigo = Integer.parseInt(txtcodigo.getText());
            if (controlador.eliminarPrestamo(codigo)) {
                JOptionPane.showMessageDialog(null, "Se borró el préstamo de la base de datos");
                this.setVisible(false);
                inicio = new VentanaLoQueMeDeben();
                inicio.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Error al eliminar el préstamo");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Formato de código incorrecto, asegurate de ingresar datos numéricos");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

