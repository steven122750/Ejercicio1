    private void btnCargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarActionPerformed
        try {

            if (txtcodigo.getText().isEmpty() || txQuienMeDebe.getText().isEmpty() || txtCantidad.getText().isEmpty() || dateFechA == null) {
                JOptionPane.showMessageDialog(null, "Llena todo el formulario para continuar");
                return;
            }

            int codigo = Integer.parseInt(txtcodigo.getText());
            String quienMeDebe = txQuienMeDebe.getText();
            float cantidad = Integer.parseInt(txtCantidad.getText());
            String fecha = ((JTextField) dateFechA.getDateEditor().getUiComponent()).getText();

            if (controlador.modificarPrestamo(codigo, quienMeDebe, fecha, cantidad)) {
                JOptionPane.showMessageDialog(null, "Se modificó el préstamo en la base de datos");
            } else {
                JOptionPane.showMessageDialog(null, "Error al modificar el préstamo");
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Formato de código o cantidad incorrectos, asegurate de ingresar datos numéricos");
        }

    }//GEN-LAST:event_btnCargarActionPerformed

